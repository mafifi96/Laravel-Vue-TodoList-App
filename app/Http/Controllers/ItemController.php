    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
       // dd($request->all());

        $task = Item::find($id);
        /* $task->completed     = $request->item['completed'] ? true : false;
        $task->completed_at  = $request->item['completed'] ? \Carbon\Carbon::now() : null;
        $item->save();
 */
         $task->update([
            'completed' => $request->item['completed'] ? true : false,
            'completed_at' => $request->item['completed'] ? \Carbon\Carbon::now() : null,
        ]);
        return $task;
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $item = Item::findOrFail($id);
        $item->delete();
        return "deleted";
    }
